 * key 와 asset 유효성 검사 계약 인터페이스.
 *
 * 주요 기능 ::
 * CanLoad(key) — 로드 가능 key 인지 입력 검증.
 * IsValid(key, asset) — 로드 결과의 최소 유효성 검사.
 *
 * 사용법 ::
 * AssetProvider 가 _GetAsync 진입과 source 로드 직후 호출. 도메인별 세부 규칙은 별도 구현체로
 * 교체 (Strategy). 기본 구현체는 DefaultAssetValidator.
 *
 * 주의 ::
 * 입력 검증과 정책 판단을 혼동하지 말 것 — 역할을 좁게 유지. provider/loader 가 신뢰할
 * 최소 조건만 보장하는 경계.
 * =========================================================
 */
#endif

namespace HUtil.AssetHandler.Validation {
    public interface IAssetValidator<TKey, TAsset> {
        bool CanLoad(TKey key);
        bool IsValid(TKey key, TAsset asset);
    }
}

