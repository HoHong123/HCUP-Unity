/* =========================================================
 * @Jason - PKH
 * RecycleView 의 공통 진입 계약 인터페이스. 외부 코드가 구체 RecycleView 변종을 모르고도
 * 공통 프로퍼티에 접근할 수 있도록 만든 marker.
 *
 * 주요 기능 ::
 * TotalContentSize — content RectTransform 이 가져야 할 전체 크기 (px). 가변 cell layout 의
 * 합산 결과로, ScrollRect 가 viewport 경계와 비교해 스크롤 가능 영역을 결정.
 *
 * 사용법 ::
 * SpanningGridRecycleView 등 RecycleView 구현체가 본 인터페이스를 구현. 외부 도메인 코드는
 * 구체 변종을 모른 채 IRecycleView.TotalContentSize 만 조회하여 layout 또는 progress 표시.
 *
 * 주의 ::
 * 본 인터페이스는 의도적으로 좁다 — 변종 간 공통 프로퍼티 1 개만 노출. 추가 기능이 필요하면
 * 구체 타입을 직접 참조하거나 새 인터페이스 추가.
 * =========================================================
 */
#endif

namespace HUI.ScrollView {
    public interface IRecycleView {
        public float TotalContentSize { get; }
    }
}

